const Gallery = () => {
  gsap.registerPlugin(ScrollTrigger);
  const ref = useRef(null);

  const Horizontalref = useRef(null);

  useLayoutEffect(() => {
    let element = ref.current;

    let scrollingElement = Horizontalref.current;

    let pinWrapWidth = scrollingElement.offsetWidth;
    let t1 = gsap.timeline();

    setTimeout(() => {
      t1.to(element, {
        scrollTrigger: {
          trigger: element,
          start: "top top",
          end: pinWrapWidth,
          scroller: ".App", //locomotive-scroll
          scrub: true,
          pin: true,
          // markers: true,
          // anticipatePin: 1,
        },
        height: `${scrollingElement.scrollWidth}px`,
        ease: "none",
      });

      t1.to(scrollingElement, {
        scrollTrigger: {
          trigger: scrollingElement,
          start: "top top",
          end: pinWrapWidth,
          scroller: ".App", //locomotive-scroll
          scrub: true,
          // markers: true,
        },
        x: -pinWrapWidth,

        ease: "none",
      });
      ScrollTrigger.refresh();
    }, 1000);
    ScrollTrigger.refresh();

    return () => {
      t1.kill();
      ScrollTrigger.kill();
    };
  }, []);

  return (
    <Section ref={ref} id="gallery">
      <Title data-scroll data-scroll-speed="-1">
        Foto's
      </Title>
      <Left></Left>
      <Right data-scroll ref={Horizontalref}>
        <Pictures img={img1} title=" " />
        <Pictures img={img2} title=" " />
        <Pictures img={img3} title=" " />
        <Pictures img={img2} title=" " />
        <Pictures img={img3} title=" " />
        <Pictures img={img2} title=" " />
        <Pictures img={img3} title=" " />
        <Pictures img={img4} title=" " />

        <a
          href="https://www.facebook.com/The.Cut.Antwerp/photos/?ref=page_internal"
          target={"_blank"}
          rel="noreferrer"
        >
          Meer foto's....
        </a>
      </Right>
    </Section>
  );
};

